/// <summary>
/// The class for feature coordinate.
/// </summary>
public class FeatureCoordinate
{
    /// <summary>
    /// Gets or sets the x in pixel.
    /// </summary>
    /// <value>
    /// The x of the feature coordinate.
    /// </value>
    public float X
    {
        get; set;
    }

    /// <summary>
    /// Gets or sets the y in pixel.
    /// </summary>
    /// <value>
    /// The y of the feature coordinate.
    /// </value>
    public float Y
    {
        get; set;
    }
}

