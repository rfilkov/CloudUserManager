    /// The moustache value.
    /// </value>
    public float Moustache
    {
        get; set;
    }

    /// <summary>
    /// Gets or sets the beard value. Represents the length of beard.
    /// </summary>
    /// <value>
    /// The beard value.
    /// </value>
    public float Beard
    {
        get; set;
    }

    /// <summary>
    /// Gets or sets the sideburns value. Represents the length of sideburns.
    /// </summary>
    /// <value>
    /// The sideburns value.
    /// </value>
    public float Sideburns
    {
        get; set;
    }
}

